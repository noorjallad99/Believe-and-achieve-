    private Boolean chickName(String names) {
        Boolean v = true;
        for (int i = 0; i < names.length(); i++) {
            if (names.charAt(i) == ' ') {
                continue;
            }
            if (!Character.isLetter(names.charAt(i))) {
                v = false;
                break;
            }
        }
        return v;
    }

