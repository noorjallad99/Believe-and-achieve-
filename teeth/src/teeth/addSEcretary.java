    private Boolean IsRepeatedEmail(String email) {
        Boolean b = false;
        String em = "";
        try {
            ps = con.prepareStatement("SELECT email FROM secretaries;");
            re = ps.executeQuery();
            while (re.next()) {
                em = re.getString("email");
                if (em.equals(email)) {
                    b = true;
                    break;
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(addSEcretary.class.getName()).log(Level.SEVERE, null, ex);
        }

        return b;
    }

